package week02_Review.practiceTasks;

public class SwapToVariables {

    public static void main(String[] args) {


        int a = 1000;
        int b = 1500;

        a =  a + b;  // a = 25
        b = a - b; // b = 10
        a = a - b;  // a = 15

        System.out.println("a = " + a);
        System.out.println("b = " + b);




    }

}
